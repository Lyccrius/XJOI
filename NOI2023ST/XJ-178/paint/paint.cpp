#include <cstdio>
#include <iostream>
#include <cstring>
int n,m,q,opt,x,c,i,j,tmp,ans[3010][100010];
using namespace std;
int main()
{
	freopen("paint.in","r",stdin);
	freopen("paint.out","w",stdout);
    cin>>tmp;
    while(tmp)
    {
        tmp--;
        memset(ans,0,sizeof(ans));
        cin>>n>>m>>q;
        for(i=1;i<=q;i++)
        {
            cin>>opt>>x>>c;
            if(opt)
                for(j=1;j<=n;j++)
                    ans[j][x]=c;
            else
                for(j=1;j<=m;j++)
                    ans[x][j]=c;
        }
        for(i=1;i<=n;i++)
        {
            for(j=1;j<=m;j++)
                cout<<ans[i][j]<<" ";
            cout<<endl;
        }
    }
    fclose(stdin);
    fclose(stdout);
    return 0;
}
